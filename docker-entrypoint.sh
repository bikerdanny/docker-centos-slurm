}

_enable_slurmd() {
  _create_slurm_config
  cat <<EOF | tee /etc/supervisord.d/slurmd.ini
[program:slurmd]
command=/usr/sbin/slurmd -D
user=root
priority=30
autostart=true
autorestart=true
EOF
}

