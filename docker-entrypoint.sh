_enable_slurmctld() {
  _create_slurm_config
  cat <<EOF | tee /etc/supervisord.d/slurmctld.ini
[program:slurmctld]
command=/usr/sbin/slurmctld -D
user=slurm
priority=20
autostart=true
autorestart=true
