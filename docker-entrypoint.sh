  _enable_munged
fi
_create_slurm_group_and_user
_create_slurm_directories
if [[ "${SLURMDBD_ENABLED}" == "true" ]]; then
  _enable_slurmdbd
fi
if [[ "${SLURMCTLD_ENABLED}" == "true" ]]; then
  _enable_slurmctld
fi
if [[ "${SLURMD_ENABLED}" == "true" ]]; then
  _enable_slurmd
fi

exec "${@}"
