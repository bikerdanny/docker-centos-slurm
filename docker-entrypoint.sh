#!/bin/bash

set -e

_enable_munged() {
  if [ ! -f /etc/munge/munge.key ]; then
    /usr/sbin/create-munge-key
  fi
  cat <<EOF | tee /etc/supervisord.d/munge.ini
[program:munged]
command=/usr/sbin/munged --foreground
user=munge
priority=1
autostart=true
autorestart=true
EOF
}

_create_slurm_group_and_user() {
  if [ ! $(getent group slurm) ]; then
    if [[ "${SLURM_GROUP}" == "" ]]; then
      SLURM_GROUP=slurm
    fi
    groupadd ${SLURM_GROUP}
  fi
  if [ ! $(getent passwd slurm) ]; then 
    if [[ "${SLURM_USER}" == "" ]]; then
      SLURM_USER=slurm
    fi
    useradd -g ${SLURM_GROUP} ${SLURM_USER}
  fi
}

_create_slurm_directories() {
  mkdir -p /etc/slurm
  chown ${SLURM_USER}:${SLURM_GROUP} /etc/slurm
  mkdir -p /var/log/slurm
  chown ${SLURM_USER}:${SLURM_GROUP} /var/log/slurm
  mkdir -p /var/run/slurm
  chown ${SLURM_USER}:${SLURM_GROUP} /var/run/slurm
  mkdir -p /var/spool/slurm/slurmctld
  chown ${SLURM_USER}:${SLURM_GROUP} /var/spool/slurm/slurmctld
  mkdir -p /var/spool/slurm/slurmd
  chown ${SLURM_USER}:${SLURM_GROUP} /var/spool/slurm/slurmd
}

